/* ---------------- 5/6. TERMS + PRIVACY (static legal pages) ---------------- */
function legal(srcFile, outFile, pageName, slug) {
  let h = read(srcFile);
  h = sub(h, '<base href="https://newly-booked.github.io/newly-booked-funnel/versions/v2/" />',
    '<!-- no <base>: fully self-contained -->', 'base tag in ' + srcFile);
  h = sub(h, '<link rel="stylesheet" href="styles.css" />',
    '<style>/* ===== styles.css (inlined) ===== */\n' + read('styles.css') + '\n  </style>', 'styles.css link in ' + srcFile);
  h = h.split('href="index.html"').join('href="' + URLS.apply + '"');
  h = h.split('href="privacy.html"').join('href="' + URLS.privacy + '"');
  h = h.split('href="terms.html"').join('href="' + URLS.terms + '"');
  emit(outFile, howTo(pageName, slug) + '\n' + h);
}

legal('terms.html', 'terms.html', 'TERMS OF SERVICE (/termsconditions)', '/termsconditions');
legal('privacy.html', 'privacy.html', 'PRIVACY POLICY (/privacy-policy-872331)', '/privacy-policy-872331');

console.log('Built ' + written.length + ' standalone pages in ghl-pages-standalone/:');
written.forEach((w) => console.log('  ' + w));
const leaks = [];
for (const n of ['apply.html', 'schedule.html', 'confirmed.html', 'dq.html', 'terms.html', 'privacy.html']) {
  const t = readFileSync(join(OUT, n), 'utf8');
  if (t.includes('github.io')) leaks.push(n);
}
console.log(leaks.length ? 'WARNING — github.io refs remain in: ' + leaks.join(', ') : 'Verified: zero github.io references in output.');
