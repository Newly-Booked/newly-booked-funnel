/* ------------- 2/3/4. SCHEDULE, CONFIRMED, DQ (self-rendering apps) ------------- */
// Each *-app.js already injects its own fonts, styles, and markup, scoped to
// its mount div — the standalone page is just the shell with the JS inlined
// instead of streamed from GitHub Pages.
function selfRendering(file, mountId, appFile, pageName, slug, title, description, robotsNoindex) {
  let app = js(appFile);
  if (appFile === 'schedule-app.js') {
    // schedule-app.js declares an unused BASE pointing at GitHub Pages; blank
    // it so the standalone page carries zero github.io references.
    app = sub(app, "var BASE = 'https://newly-booked.github.io/newly-booked-funnel/versions/v2/';",
      "var BASE = ''; /* unused; blanked by build-standalone */", 'unused BASE in schedule-app.js');
  }
  const html = [
    howTo(pageName, slug),
    '<!doctype html>',
    '<html lang="en">',
    '<head>',
    '  <meta charset="utf-8" />',
    '  <meta name="viewport" content="width=device-width, initial-scale=1" />',
    '  <title>' + title + '</title>',
    '  <meta name="description" content="' + description + '" />',
    ...(robotsNoindex ? ['  <meta name="robots" content="noindex" />'] : []),
    '</head>',
    '<body>',
    '  <div id="' + mountId + '"></div>',
    '  <script>/* ===== ' + appFile + ' (inlined) ===== */',
    app,
    '  </script>',
    '</body>',
    '</html>',
  ].join('\n');
  emit(file, html);
}

selfRendering('schedule.html', 'nb-schedule', 'schedule-app.js',
  'SCHEDULE PAGE (/schedule-822049)', '/schedule-822049',
  'Newly Booked · Pick your time',
  'You qualified. Book your Newly Booked 15-minute intro call with a patient generation specialist.', true);

